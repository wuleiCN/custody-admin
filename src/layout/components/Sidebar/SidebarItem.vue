<script>
import path from 'path'
import { isExternal } from '@/utils/validate'
import Item from './Item'
import AppLink from './Link'
import FixiOSBug from './FixiOSBug'

export default {
  name: 'SidebarItem',
  components: { Item, AppLink },
  mixins: [FixiOSBug],
  props: {
    // route object
    item: {
      type: Object,
      required: true
    },
    isNest: {
      type: Boolean,
      default: false
    },
    basePath: {
      type: String,
      default: ''
    }
  },
  data() {
    // To fix https://github.com/PanJiaChen/vue-admin-template/issues/237
    // TODO: refactor with render function
    this.onlyOneChild = null
    return {}
  },
  methods: {
    hasOneShowingChild(children = [], parent) {
      const showingChildren = children.filter(item => {
        if (item.hidden) {
          return false
        } else {
          // Temp set(will be used if only has one showing child)
          this.onlyOneChild = item
          return true
        }
      })

      // When there is only one child router, the child router is displayed by default
      if (showingChildren.length === 1) {
        return true
      }

      // Show parent if there are no child router to display
      if (showingChildren.length === 0) {
        this.onlyOneChild = { ... parent, noShowingChildren: true }
        return true
      }

      return false
    },
    resolvePath(routePath) {
      console.log(routePath)
      if (isExternal(routePath)) {
        return routePath
      }
      if (isExternal(this.basePath)) {
        return this.basePath
      }
      if (this.item.redirect === '/dashboard') {
        return '/dashboard'
      }
      return path.resolve(this.basePath, routePath)
    },
    resolveMenuPath(children) {
      if (children.children && children.children.length > 0) {
        console.log(path.resolve(this.item.path, children.path, children.children[0].path))
        return path.resolve(this.item.path, children.path, children.children[0].path)
      } else {
        console.log(path.resolve(this.item.path, children.path))
        return path.resolve(this.item.path, children.path)
      }
    },
    setCurMenu(route) {
      // this.$store.dispatch('app/setCurMenu', route)
      if ((route.children && route.children.length === 1) || !route.children) {
        this.$store.dispatch('setCurSecondPath', '')
        this.$store.dispatch('setCurRouteChildren', [])
      } else {
        this.$store.dispatch('setCurSecondPath', route.path)
        this.$store.dispatch('setCurRouteChildren', route.children)
      }
    }
  }
}
</script>
